package TSim;

public class TSwitch {
    private int xPos;
    private int yPos;

    public TSwitch(int xPos, int yPos) {
        this.xPos = xPos;
        this.yPos = yPos;
    }

    public int getX() {
        return xPos;
    }

    public int getY() {
        return yPos;
    }
}
